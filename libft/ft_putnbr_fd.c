#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	l;

	l = n;
	if (l < 0)
	{
		write(fd, "-", 1);
		l = -l;
	}
	if (l / 10)
		ft_putnbr_fd(l / 10, fd);
	l = l % 10 + '0';
	write(fd, &l, 1);
}
